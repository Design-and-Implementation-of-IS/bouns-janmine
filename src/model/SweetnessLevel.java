package model;

public class SweetnessLevel {
    private int sweetnessLevelId;
    private String sweetnessValue;

    public int getSweetnessLevelId() {
        return sweetnessLevelId;
    }

    public void setSweetnessLevelId(int sweetnessLevelId) {
        this.sweetnessLevelId = sweetnessLevelId;
    }

    public String getSweetnessValue() {
        return sweetnessValue;
    }

    public void setSweetnessValue(String sweetnessValue) {
        this.sweetnessValue = sweetnessValue;
    }
}
